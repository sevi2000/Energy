package fr.uparis.energy.utils;

/**
 * This class represents coordinates of a 2D array element.
 */
public class IntPair {

    /**
     * Row.
     */
    public final int a;

    /**
     * Column.
     */
    public final int b;

    /**
     * Class constructor.
     * @param a row
     * @param b column
     */
    public IntPair(int a, int b) {
        this.a = a;
        this.b = b;
    }
}
